public class Leaf {
    public double avg;
    public double max;
    public double val = 0;

    public void setAvg(double avg) {
        this.avg = avg;
    }
    public void setMax(double max){
        this.max = max;
    }

    public double getAvg() {
        return avg;
    }

    public double getMax() {
        return max;
    }

    public void setVal(double val) {
        this.val = val;
    }

    public double getVal() {
        return val;
    }
}
